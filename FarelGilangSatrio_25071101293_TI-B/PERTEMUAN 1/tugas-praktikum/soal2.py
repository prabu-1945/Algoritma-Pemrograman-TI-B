'''
Soal: Buat sebuah program Python yang:
1. Menyimpan dua bilangan ke dalam dua variabel.
2. Menghitung hasil penjumlahan dan perkalian dari kedua bilangan tersebut.
3. Menampilkan hasilnya ke layar.
Contoh keluaran :
Hasil penjumlahan = 15
Hasil penjumlahan = 50
'''
# Menyimpan dua bilangan
x = 10
y = 5

# Menghitung hasil
penjumlahan = x + y
perkalian = x * y

# Menampilkan hasil
print("Hasil penjumlahan =", penjumlahan)
print("Hasil perkalian =", perkalian)