#BOOLEANS
x = 75
y = 75

if x > y: #bandingkan kalau x lebih besar dari y
  print("x is greater than y") 
if x < y: #bandingkan kalau x lebih kecil dari y
  print("x is not greater than y") 
if x == y : #bandingkan kalau x dan y sama
  print("x and y are the same value")

print(bool("Nama")) # Selalu true karena ada value tapi tidak ada pebandingnya
print(bool(67))
