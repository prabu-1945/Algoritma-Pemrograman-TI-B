#Comparison Operators
x = 5
y = 3

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

#operators and
x = 7

print(x > 0 and x < 30)


#operators or
x = 7

print(x < 7 or x > 30)


#operators not
