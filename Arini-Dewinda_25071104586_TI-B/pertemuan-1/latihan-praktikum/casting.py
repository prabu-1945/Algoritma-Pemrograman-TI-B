#bab casting

"""
Casting adalah proses mengubah satu tipe data ke tipe data lain.
Misalnya mengubah string menjadi integer dan integer menjadi float.
"""

tinggi_str = "186"

#ubah ke int
tinggi_int = int(tinggi_str)

#ubah int ke float
tinggi_float = float(tinggi_int)

print("Tinggi : ", tinggi_str)
print("Tinggi : ", tinggi_int)
print("Tinggi : ", tinggi_float)