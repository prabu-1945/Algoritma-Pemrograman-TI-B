#bab tipe data

#string(teks)
nama = "Arin"

#integer (bilangan bulat)
umur = 18

#float (bilangan desimal)
tinggi = 153.2

#boolean (true / false)
sedang_pusreng = True

#output tipe data dan nilainya
print("Nama:", nama)
print("Umur:", umur)
print("Tinggi badan:", tinggi)
print("Sedang belajar:", sedang_pusreng)

#cek tipe data
print(type(nama))
print(type(umur))
print(type(tinggi))
print(type(sedang_pusreng))
